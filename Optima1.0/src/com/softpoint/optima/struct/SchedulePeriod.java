package com.softpoint.optima.struct;


public class  SchedulePeriod
{
	private Period current = null;
	private Period next = null;
	
	
	public Period getCurrent() {
		return current;
	}
	public void setCurrent(Period current) {
		this.current = current;
	}
	public Period getNext() {
		return next;
	}
	public void setNext(Period next) {
		this.next = next;
	}
			
}
